package day03;

public class OperDemo2 {
    public static void main(String[] args) {
        int a = 5, b = 10, c = 5;
        boolean b1 = a > b;
        System.out.println(b1);
        System.out.println(a < b);
        System.out.println(a == c);
        System.out.println(a != c);
        System.out.println(a % 2 == 1);
        System.out.println(a++ == 5);
        System.out.println(a == 5);
        System.out.println(--a == 5);
        System.out.println(a == 5);
    }
}
